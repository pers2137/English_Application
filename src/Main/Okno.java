    private void conf_change_Path(String name)
    {
        ArrayList<String> temp = new ArrayList<>();
        File config = new File("config.conf");
        //odczyt konfiguracji z dokonaniem edycji.
        try 
            {
               
                BufferedReader buffer = new BufferedReader(new InputStreamReader(new FileInputStream(config)));
                String line;
                String first;
                boolean checker_options = false ;
                
                    while ((line = buffer.readLine()) !=null)
                    { 
                        
                        int xd = line.indexOf(':');
                        int dd = line.length();

                       first=line.substring(0, xd);

                       if(first.equals("Path")) 
                       {
                           temp.add(first+":"+name);
                           checker_options = true;
                           
                       }
                       else
                       {
                           temp.add(line);
                       }
                     
                    }
                    
                    if(checker_options == false)  temp.add("Path:"+name);
                    
                    buffer.close();
            } 
            catch (FileNotFoundException e) 
            {
                System.out.println("Coś poszło nie tak z odczytem konfiguracji !");
            }
             catch (IOException e) 
            {
                System.out.println("Coś poszło nie tak z wczytywaniem lini !");
            }
        
        
        //ponowny zapis konfiguracji do pliku.
        try 
        {
            FileWriter write = new FileWriter(config, false);
      
            for(int i=0;i<temp.size();i++)
            {
                write.append(temp.get(i));
            }
            
            write.close();
        } 
        catch (IOException e) 
        {
            System.out.println("Błąd zapisu konfiguracji do pliku.");
        }
        
        
        
        
    }
     
    private void conf_read()
    {
            nazwa_pliku = "";
        
        try 
            {
                File config = new File("config.conf");
                BufferedReader buffer = new BufferedReader(new InputStreamReader(new FileInputStream(config)));
                String line;
                String first,second;
                
                    while ((line = buffer.readLine()) !=null)
                    { 
                        
                        int xd = line.indexOf(':');
                        int dd = line.length();

                       first=line.substring(0, xd);
                       second=line.substring(xd+1, dd);
                       if(first.equals("Path")) 
                       {
                           if(second != null  && second.length()>0)
                           {
                               nazwa_pliku = second;
                           }
                       }
                     
                    }
                    buffer.close();
            } 
            catch (FileNotFoundException e) 
            {
                System.out.println("Coś poszło nie tak z odczytem konfiguracji !");
            }
             catch (IOException e) 
            {
                System.out.println("Coś poszło nie tak z wczytywaniem lini !");
            }
           
    }
    
    private void startup_config() 
    {
        File config = new File("config.conf");
        File learned_words = new File("learned_words.txt");
        
        //SPRAWDZENIE CZY PLIKI ISTNIEJĄ
        if(!config.exists())
        {
            
            try 
            {
                 config.createNewFile();
                     
            } 
            catch (IOException e) 
            {
                System.out.println("Nie udało się stworzyć pliku konfiguracyjnego!.");
            }
           
        }
        //JEŚLI ISTNIEJE SPRÓBUJ ODCZYTAĆ KONFIGURAJCĘ
        else
        { 
           conf_read();
        }
        
        if(!learned_words.exists())
        {
            try 
            {
                 learned_words.createNewFile();           
            } 
            catch (Exception e) 
            {
                System.out.println("Nie udało się stworzyć pliku na wyuczone słówka!.");
            }
           
        }
        
       
    }
    
    
