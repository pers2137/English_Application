        public void losuj_slowko_test(ArrayList<String> ang, ArrayList<String> pol, int tryb)
       {
        if( ang.isEmpty()) file_read(); 
      
        JToggleButton [] tab;
        JToggleButton [] tab2 = {odp_19,odp_20,odp_21,odp_22,odp_23,odp_24}; //POL TRYB
        JToggleButton [] tab3 = {odp_13,odp_14,odp_15,odp_16,odp_17,odp_18};    //ENG TRYB
       
        Random r = new Random();
        if(tryb == 0) //POLSKI TRYB - 0
        {
           tab=tab2;
        }
        else         //ANGIELSKI TRYB - 1
        {
            
            tab=tab3;
        }
        
        // losowanie polskiego slowka
       
        boolean repeat = true;
        int index;
        //Sprawdzenie czy dane pytanie już było w tym cyklu.
        do
        {
            index = r.nextInt(pol.size());
            repeat = check_repeat(index);
         
           
        }
        while(repeat);
        
        //DODANIE DO TALIBY AKTUALNIE WYŚWIETLANEJ ODPOWIEDZI
        test_ang.add(ang.get(index));
         
  
        if(tryb==0) slowko3.setText(pol.get(index));   
        else slowko2.setText(pol.get(index));
                
        
        //sprawdzenie czy w słowniku jest więcej niż 6 słówek jeśli tak losnuj zgodnie ze sztuką
        if(ang.size() > 5)
        {
            int i=0;
            while(i<6)
            {    
                //losowanie kolejnego slowka
                int temp = r.nextInt(ang.size());
                //sprawdzenie czy te slowko to nie odpowiedz
                if(temp == index) {}
                //jesli te slowko to nie odpowiedz to wykonaj
                else
                {
                    boolean temp_uzyta = false;
                    //sprawdzenie czy nie dana opcja odpowiedzi została już użyta, aby się nie powtórzyły.
                    for(int j=0;j<i;j++)
                    {
                        if(tab[j].getText().equals(ang.get(temp))) temp_uzyta = true;
                    }
                    
                    if(temp_uzyta == false)
                    {
                        tab[i].setText(ang.get(temp));
                        i++;
                    }
                }
            }
        }
        //jeśli w słowniku jest mniej niż 6 słówek losuj jak popadnie.
        else
        {
            for(int i=0;i<6;i++)
            {            
                 tab[i].setText(ang.get(r.nextInt(ang.size())));
            }
        }
        
        
        //ustawienie w odpowiedziach poprawnej odpowiedzi
         poprawna = r.nextInt(6);    
         
        tab[poprawna].setText(ang.get(index));
        if(tryb==0) jLabel6.setText(""); //ustawienie labela na pusty.
        else jLabel14.setText(""); //ustawienie labela na pusty.
         
        //  poprawna - index odpowiedniej odpowiedzi
        //  index - index wybranego slowka
        for(int i=0;i<6;i++)
        {
            tab[i].setBackground(Color.getHSBColor(50,50,250));
        }
       
       
        
    }

        public boolean check_repeat(int index)
        {

            if(test_ang == null) return false;


            for(int i=0;i<test_ang.size();i++)
            {
                if(ang.get(index).equals(test_ang.get(i))){
                    return true;
                }
            }

            return false;


        }

